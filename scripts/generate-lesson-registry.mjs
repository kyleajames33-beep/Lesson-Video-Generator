import {readdirSync, readFileSync, writeFileSync} from 'node:fs';
import path from 'node:path';
import {getCompositionId} from './lesson-utils.mjs';

const dataDir = path.resolve('src/data');
const outputPath = path.resolve('src/data/lessonRegistry.ts');

const files = readdirSync(dataDir)
  .filter((file) => file.endsWith('.json'))
  .sort();

const importNameFor = (index) => `lesson${index + 1}Data`;

const lessons = files.map((file, index) => {
  const lesson = JSON.parse(readFileSync(path.join(dataDir, file), 'utf8'));
  const id = getCompositionId(lesson, index + 1);

  return {file, id, importName: importNameFor(index)};
});

const seenIds = new Map();
for (const lesson of lessons) {
  const existing = seenIds.get(lesson.id);
  if (existing) {
    console.error(`Duplicate composition id "${lesson.id}" from ${existing} and ${lesson.file}`);
    process.exit(1);
  }
  seenIds.set(lesson.id, lesson.file);
}

const contents = `// Auto-generated by scripts/generate-lesson-registry.mjs. Do not edit by hand.
import type {LessonData} from '../lesson/types';
${lessons.map(({file, importName}) => `import ${importName} from './${file}';`).join('\n')}

export const lessons: {id: string; data: LessonData}[] = [
${lessons.map(({id, importName}) => `  {id: '${id}', data: ${importName} as LessonData},`).join('\n')}
];
`;

writeFileSync(outputPath, contents);
console.log(`Generated ${path.relative(process.cwd(), outputPath)} with ${lessons.length} lesson(s).`);
